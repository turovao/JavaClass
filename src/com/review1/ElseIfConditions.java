public class ElseIfConditions {
	public static void main(String[] args) {
		 int time= 1922;
				
		 
		 if(time<=0700) {
			 System.out.println("Good morning");
			 
		 }else if(time<=1205) {
			 System.out.println("Good afternoon");
		 }else {
			 System.out.println("Good evening");
		 }
		 
		
	}

}
