package com.class2;

public class VariableChange {
	public static void main(String[] args){
		//Values of variables can be change 
		String firstName ="Olga";
		firstName ="Nadia";
		
		System.out.println(firstName);
		
		String lastName = "Turova";
		lastName="Ali"; 
		
		char grade ='B';
		
		//char grade='A'; do not work
		
		grade='A';
		
		// creating a variable & assigning the value
		
		String city = "Fairfax";
		
		city="New_York"; // value of variable is change
		
		System.out.println(grade);
		
		System.out.println(city);
		// changing value of city from Fairfax to NewYork
		
		
		System.out.println(lastName);
	}


}
