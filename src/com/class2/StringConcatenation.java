package com.class2;

public class StringConcatenation {
	public static void main(String[] args) {

		String firstName = "Olga";

		String lastName = "Turova";

		System.out.println(" My firstname is " + firstName + "." + " My lastName is " + lastName + ".");

		char grade = 'A';

		System.out.println(" My grade is " + grade);

		String city = "Fairfax";
		String state = "VA";

		System.out.println(" My city is " + city +"."+ " The state i live in " + state+".");

		long phoneNumber = 8329383866l;

		System.out.println(" My phoneNumber is " + phoneNumber);

	}

}
