package com.class7;

public class IncrementAndDecrement {
	public static void main(String[] args) {
		
		
		// post incremnt  a++
		
		
	         int a=299;
	         int b=a++;// first assign -> increment
	        
	        System.out.println(a);//300
	        System.out.println(b);//299
	        
	    	// post decremnt  b--
	        
	        int z=10;
	        int c=z--;//10-1
	        System.out.println(z);//9
	        System.out.println(c);//10
	        
	         
	         
	         
		
	}
	
	
         
         
         
         
}
