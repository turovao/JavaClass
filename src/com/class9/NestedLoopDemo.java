package com.class9;

public class NestedLoopDemo {
	public static void main(String[] args) {
		
for ( int i=1; i<=3; i++ ) {
			
			for  (int y=1; y<=3; y++ ) {
				
			
				System.out.println(i+ " "+y);// 11; 12;12;// 21; 22;23;//31;32;33;
			}
		}
		     // i=1; y=0// 10;11;12;13// 20;21;22;23
}

		
	}


