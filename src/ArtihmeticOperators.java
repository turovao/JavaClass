
public class ArtihmeticOperators {

	public static void main(String[] args) {

		// declare 2 int variables and then we perform:
		// addition, subtruction, multiplication, division

		int a = 30;
		int b = 15;

		System.out.println(a + b); // 45
		System.out.println(a - b); // 15
		System.out.println(a * b); // 450
		System.out.println(a / b); // 2

		int sum = a + b;
		int sub = a - b;
		int mult = a * b;
		int div = a / b;

		System.out.print(sum); // 45
		System.out.print(sub); // 15
		System.out.print(mult); // 450
		System.out.print(div); // 2

		System.out.println(" the sum of 2 numbers is " + sum+".");
		System.out.println(" the sub of 2 numbers is " + sub);
		System.out.println(" the mult of 2 numbers is " + mult);
		System.out.println(" the div of 2 numbers is " + div);

	}

}
